let months = ["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"];

for (let i = 0; i < months.length; i++){
    document.writeln("<div class='paintDiv'>" + months[i] + "</div>");
    let m = document.getElementsByClassName('paintDiv');
    m[i].style.height = "40px";
    m[i].style.fontSize = "30px";
    m[i].style.fontWeight = "bold";
    m[i].style.textAlign = "center";
    m[i].style.lineHeight = "40px";
    m[i].style.borderRadius = "10px";
    m[i].style.marginBottom = "5px";
    createColorBackground(m[i]);
    createColorLetters(m[i]);
}

function createColorBackground(back){
    let r = Math.floor(Math.random() * 256);
    let g = Math.floor(Math.random() * 256);
    let b = Math.floor(Math.random() * 256);
    back.style.background = "rgb(" + r +", " + g +", " + b +")";
}
function createColorLetters(letters){
    let r = Math.floor(Math.random() * 256);
    let g = Math.floor(Math.random() * 256);
    let b = Math.floor(Math.random() * 256);
    letters.style.color = "rgb(" + r +", " + g +", " + b +")";
}